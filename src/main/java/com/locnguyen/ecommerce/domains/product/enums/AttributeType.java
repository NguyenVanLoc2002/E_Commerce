package com.locnguyen.ecommerce.domains.product.enums;

/**
 * Attribute classification.
 *
 * <ul>
 *   <li>{@code VARIANT} — used to generate product variants (e.g., size, color)</li>
 *   <li>{@code DESCRIPTIVE} — informational only (e.g., material, fit, sleeve type)</li>
 * </ul>
 */
public enum AttributeType {
    VARIANT,
    DESCRIPTIVE
}
